'''
코드 설명: 1부터 100 사이의 랜덤 숫자를 하나 생성한 뒤(random.randint(1, 100)), 
사용자가 해당 숫자를 맞출 때까지 반복해서 입력을 받는 게임입니다. 
사용자가 추측한 숫자(guess)가 정답(number)보다 작으면 "너무 작아요", 
크면 "너무 커요"라고 힌트를 주고, 맞추면 "정답입니다!"를 출력하고 루프를 종료하는 것이 목적입니다. 
이론상 while True 루프 내에서 break를 만나면 게임이 끝나도록 설계해야 합니다.

'''
import random

# 1부터 100 사이의 숫자를 무작위로 선택
number = random.randint(1, 100)

while True:
    guess = int(input("1부터 100 사이의 숫자를 맞혀보세요: "))
    
    if guess < number:
        print("너무 작아요.")
    elif guess > number:
        print("너무 커요.")
    else:
        print("정답입니다!")
        break  # 정답을 맞히면 루프 종료
# 이 코드는 사용자가 1부터 100 사이의 숫자를 맞추는 게임입니다.
# 사용자가 입력한 숫자가 정답보다 작거나 크면 힌트를 주고,
# 정답을 맞히면 "정답입니다!"를 출력하고 게임을 종료합니다.
# 이 코드는 무한 루프를 사용하여 사용자가 정답을 맞힐 때까지 계속해서 입력을 받습니다.
